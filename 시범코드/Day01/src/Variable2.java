// 변수 초기화 하기
public class Variable2 {
    public static void main(String[] args) {
        int level = 10; // 정수형 변수 level 선언과 동시에 값을 대입(초기화)
        int box = 20;
        String str = "문자";
        System.out.println(level);
        System.out.println(box);
        System.out.println(str);
    }
}
