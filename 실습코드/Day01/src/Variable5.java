public class Variable5 {
    public static void main(String[] args) {
        int a = 123;
        int b = 456;

        // 수정 코드 작성 시작
        a = 456;
        b = 123;
        // 수정 코드 작성 끝

        System.out.println("a :" + a);
        // a : 456
        System.out.println("b :" + b);
        // b : 123
    }
}
